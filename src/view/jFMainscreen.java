    private void jTClientesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTClientesKeyReleased
        
        if (jTClientes.getSelectedRow() != -1){
            
            NomeCliente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 1).toString());
            Logradouro.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 2).toString());
            Numero.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 3).toString());
            ContaCorrente.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 4).toString());
            CPF.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 5).toString());
            
        }
        
    }//GEN-LAST:event_jTClientesKeyReleased

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
         if (jTClientes.getSelectedRow() != -1) {

            produto p = new produto();
            ProdutoDAO dao = new ProdutoDAO();

            p.setNome_cliente((NomeCliente.getText()));
            p.setLogradouro(Logradouro.getText());
            p.setNumero_Logra(Integer.parseInt(Numero.getText()));
            p.setCC(Float.parseFloat(ContaCorrente.getText()));
            p.setCPF(Float.parseFloat(CPF.getText()));
            p.setId((int) jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));
            dao.update(p);

            NomeCliente.setText("");
            Logradouro.setText("");
            Numero.setText("");
            ContaCorrente.setText("");
            CPF.setText("");

            readJTable();

        }
    }//GEN-LAST:event_jButton2ActionPerformed

