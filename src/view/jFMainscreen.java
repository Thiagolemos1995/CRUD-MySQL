    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTClientes.getModel();
        modelo.setNumRows(0);
        ProdutoDAO pdao = new ProdutoDAO();

        for (produto p : pdao.read()) {

            modelo.addRow(new Object[]{
                p.getId(),
                p.getNome_cliente(),
                p.getLogradouro(),
                p.getNumero_logra(),
                p.getCC(),
                p.getCPF()
            });

        }

    }
